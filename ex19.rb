def cheese_and_crackers(cheese_count,crackers_count)
  puts "you have #{cheese_count} of cheeses"
  puts "you have #{crackers_count} of boxes"
end
puts "directly give the input numbers"
cheese_and_crackers(10,50)
puts "give the input function variables"
amount_of_cheese=54
amount_of_boxes=65
cheese_and_crackers(amount_of_cheese,amount_of_boxes)

puts "we are math even do"
cheese_and_crackers(5+8,6+5)

puts "we can combine both variables and math"
cheese_and_crackers(amount_of_cheese+10,amount_of_boxes+6000)
 
